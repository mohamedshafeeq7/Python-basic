def fibonacci_series(limit):
    fib_series = []
    a, b = 0, 1
    while a < limit:
        fib_series.append(a)
        a, b = b, a + b
    return fib_series

# Example usage:
limit = int(input("Enter the limit for the Fibonacci series: "))
result = fibonacci_series(limit)
print("The Fibonacci series up to", limit, "is:", result)
